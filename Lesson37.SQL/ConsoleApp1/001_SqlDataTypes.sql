-- a adlı Int tipində dəyişən yaradırıq və ona 2 dəyərini mənimsədirik
DECLARE @A INT = 2;

-- @A dəyişəninin dəyərini ekrana çıxarırıq
PRINT @A;   -- 2

-- Dəyişənin dəyərini dəyişirik
SET @A = 7;

-- @A dəyişəninin dəyərini ekrana çıxarırıq
PRINT @A;   -- 7